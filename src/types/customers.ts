export type Customer = {
  id: number;
  name: string;
  primary_contact_name: string | null;
  primary_contact_email: string | null;
  primary_contact_phone: string | null;
  credit_limit: number | null;
  payment_terms: number | null;
  consolidated_invoicing: boolean;
  created_at: string;
  updated_at: string;
  created_by: string | null;
  updated_by: string | null;
};

export type CustomerLocation = {
  id: number;
  customer_id: number;
  location_name: string | null;
  address_line1: string | null;
  address_line2: string | null;
  city: string | null;
  state: string | null;
  postal_code: string | null;
  country: string;
  created_at: string;
  updated_at: string;
  created_by: string | null;
  updated_by: string | null;
};

export type CustomerWithLocations = Customer & {
  customer_locations?: CustomerLocation[];
};

export type CreateCustomerData = {
  name: string;
  primary_contact_name?: string;
  primary_contact_email?: string;
  primary_contact_phone?: string;
  credit_limit?: number;
  payment_terms?: number;
  consolidated_invoicing?: boolean;
  locations?: CreateCustomerLocationData[];
};

export type CreateCustomerLocationData = {
  location_name?: string;
  address_line1?: string;
  address_line2?: string;
  city?: string;
  state?: string;
  postal_code?: string;
  country?: string;
};

export type UpdateCustomerData = Partial<CreateCustomerData>;

export type CustomerFilters = {
  search?: string;
  payment_terms?: number | null;
  consolidated_invoicing?: boolean | null;
  has_credit_limit?: boolean | null;
  state?: string;
};

